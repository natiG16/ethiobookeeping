@Injectable({ providedIn: 'root' })
export class LocaleService {
  private readonly http = inject(HttpClient);
  readonly locale = signal<'en' | 'am'>(
    (localStorage.getItem('locale') as 'en' | 'am') || 'en'
  );
  readonly translations = signal<Record<string, string>>(bundledEn);

  constructor() {
    this.applyDocumentLocale(this.locale());
    this.load();
  }

  t(key: string, params?: Record<string, string | number>): string {
    let text = this.translations()[key] || key;
    if (params) {
      for (const [k, v] of Object.entries(params)) {
        text = text.replaceAll(`{${k}}`, String(v));
      }
    }
    return text;
  }

  setLocale(loc: 'en' | 'am') {
    this.locale.set(loc);
    localStorage.setItem('locale', loc);
    this.applyDocumentLocale(loc);
    this.translations.set(loc === 'am' ? { ...bundledAm } : { ...bundledEn });
    this.load();
  }

  /** Switch UI fonts and document language for Amharic vs English. */
  private applyDocumentLocale(loc: 'en' | 'am'): void {
    if (typeof document === 'undefined') return;
    const html = document.documentElement;
    html.lang = loc === 'am' ? 'am' : 'en';
    html.classList.toggle('locale-am', loc === 'am');
    html.classList.toggle('locale-en', loc === 'en');
  }

  private load() {
    this.http
      .get<ApiResponse<Record<string, string>>>(
        `${environment.apiUrl}/localization/${this.locale()}`
      )
      .subscribe({
        next: (res) => {
          const base = this.locale() === 'am' ? bundledAm : bundledEn;
          this.translations.set({ ...base, ...res.data });
        },
        error: () => {},
      });
  }
}
